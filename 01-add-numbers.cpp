// This C++ Program adds two numbers.

#include <iostream>
using namespace std;

int main() {
    int firstNumber = 23;
    int secondNumber = 7;

    int sumTotal = firstNumber + secondNumber;

    cout << "The sum of " << firstNumber << " and " 
    << secondNumber << " is " << sumTotal << ".";
    return 0;
}

// OUTPUT
// The sum of 23 and 7 is 30.