// This C++ Program adds two numbers

#include <iostream>
using namespace std;

int main() {
    int firstNumber = 23;
    int secondNumber = 7;

    // Add the two numbers
    int sumTotal;
    sumTotal = firstNumber + secondNumber;

    // Display the sum
    cout << "The sum of " << firstNumber << " and " << secondNumber << " is " << sumTotal << ".";
    return 0;
}

// OUTPUT
// The sum of 23 and 7 is 30.